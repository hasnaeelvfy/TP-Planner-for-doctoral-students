/**
 * Resolve UI language from session or cookie.
 */
function init_ui_lang(): void {
    $allowed = I18N_ALLOWED;
    if (isset($_GET['lang']) && in_array($_GET['lang'], $allowed, true)) {
        $_SESSION['ui_lang'] = $_GET['lang'];
        i18n_set_lang_cookie($_GET['lang']);
        return;
    }
    if (!empty($_SESSION['ui_lang']) && in_array($_SESSION['ui_lang'], $allowed, true)) {
        return;
    }
    $c = $_COOKIE['ui_lang'] ?? '';
    if (in_array($c, $allowed, true)) {
        $_SESSION['ui_lang'] = $c;
        return;
    }
    $_SESSION['ui_lang'] = 'fr';
}

function i18n_lang_cookie_path(): string {
    if (defined('HTTP_COOKIE_PATH') && HTTP_COOKIE_PATH !== '') {
        return HTTP_COOKIE_PATH;
    }
    return '/';
}

function i18n_set_lang_cookie(string $lang): void {
    $path = i18n_lang_cookie_path();
    $expires = time() + 365 * 86400;
    $secure = !empty($_SERVER['HTTPS']) && $_SERVER['HTTPS'] !== 'off';
    if (PHP_VERSION_ID >= 70300) {
        $opts = [
            'expires' => $expires,
            'path' => $path,
            'secure' => $secure,
            'httponly' => true,
            'samesite' => 'Lax',
        ];
        setcookie('ui_lang', $lang, $opts);
    } else {
        setcookie('ui_lang', $lang, $expires, $path, '', false, true);
    }
}

function get_ui_lang(): string {
    $l = $_SESSION['ui_lang'] ?? 'fr';
    return in_array($l, I18N_ALLOWED, true) ? $l : 'fr';
}

function t(string $key): string {
    $lang = get_ui_lang();
    $dict = $GLOBALS['I18N_STRINGS'][$lang] ?? [];
    if (isset($dict[$key])) {
        return $dict[$key];
    }
    $fallback = $GLOBALS['I18N_STRINGS']['en'][$key] ?? $key;
    return $fallback;
}

/** Role label for navbar badge */
function format_role_label(?string $role): string {
    if ($role === null || $role === '') {
        return '';
    }
    $n = normalize_user_role($role);
    $key = $n === 'admin' ? 'role.admin' : 'role.trainee_teacher';
    return t($key);
}

function i18n_export_all(): array {
    return $GLOBALS['I18N_STRINGS'];
}
